// if statement

let num1: number = 10;
let num2: number = 40;
let num3: number = 30;

// condition (ternary operation)

num1 < num2
    ? console.log(`${num1} is less than ${num2}`)
    : console.log(`${num1} is greater than ${num2}`)

if(num1 < num2){
    console.log(`${num1} is less than ${num2}`)
}else{
    console.log(`${num1} is greater than ${num2}`)
}

if(num2 < num3){
    console.log(`${num2} is less than ${num3}`)
}else{
    console.log(`${num2} is greater than ${num3}`)
}


